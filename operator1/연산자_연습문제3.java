package jungol.operator1;

import java.util.Scanner;

public class 연산자_연습문제3 {
    public static void main(String[] args) {

        int a = 10;
        int b = 10;

        System.out.println("a = " + a + " b = " + b);

        a++;
        ++b;
        System.out.println("a = " + a + " b = " + b);

    }
}
